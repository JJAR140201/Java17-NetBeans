    private boolean validarCredenciales(String correo, String contraseña) {
        boolean credencialesValidas = false;
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/proyecto_final", "root", "root");
            String sql = "SELECT * FROM inicioSesion WHERE correoElectronico = ? AND contrasena = ?";
            PreparedStatement statement = connection.prepareStatement(sql);
            statement.setString(1, correo);
            statement.setString(2, contraseña);
            ResultSet resultSet = statement.executeQuery();
            credencialesValidas = resultSet.next();
            connection.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al validar las credenciales: " + ex.getMessage());
        }
        return credencialesValidas;
    }

