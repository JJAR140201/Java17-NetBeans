    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // Paso 0: Verificar que los campos no estén vacíos
        if (txtCorreo.getText().isEmpty() || txtContraseña.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete tanto el correo electrónico como la contraseña para iniciar sesión.");
            return; // Salir del método si algún campo está vacío
        }

        String correo = txtCorreo.getText();
        String contraseña = txtContraseña.getText();
        boolean credencialesValidas = validarCredenciales(correo, contraseña);
        if (credencialesValidas) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso");
            
            dispose(); // Cerrar la ventana actual
            new Consulta().setVisible(true); // Mostrar el JFrame de Consulta
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

